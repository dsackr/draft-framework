def workspace_repository_config(workspace: Path) -> dict[str, Any]:
    config_path = workspace_config_path(workspace)
    config = read_yaml(config_path) if config_path.exists() else {}
    repository = config.get("repository")
    if not isinstance(repository, dict):
        repository = {}
    return {
        "defaultBranch": repository.get("defaultBranch") or "main",
        "devBranch": repository.get("devBranch") or "draft-dev",
        "publishMode": repository.get("publishMode") or "pull-request",
    }


def git_stdout(command: list[str], cwd: Path) -> str:
    completed = run(command, cwd)
    if completed.returncode != 0:
        raise HTTPException(status_code=500, detail=completed.stderr or completed.stdout or f"Command failed: {' '.join(command)}")
    return completed.stdout.strip()


def ensure_working_branch(workspace: Path) -> str:
    repo = workspace_repository_config(workspace)
    default_branch = str(repo["defaultBranch"])
    dev_branch = str(repo["devBranch"])
    current = run(["git", "branch", "--show-current"], workspace)
    branch = current.stdout.strip() if current.returncode == 0 else ""
    if branch and branch != default_branch:
        return branch
    if not dev_branch:
        raise HTTPException(status_code=400, detail="repository.devBranch is required for app-managed writes")
    remote = run(["git", "show-ref", "--verify", "--quiet", f"refs/remotes/origin/{dev_branch}"], workspace)
    if remote.returncode == 0:
        git_stdout(["git", "checkout", dev_branch], workspace)
    else:
        git_stdout(["git", "checkout", "-B", dev_branch], workspace)
    return dev_branch


