def slug(value: str) -> str:
    return re.sub(r"[^a-z0-9.-]+", "-", value.lower()).strip(".-") or "object"


def safe_workspace_path(workspace: Path, relative_path: str) -> Path:
    candidate = (workspace / relative_path).resolve()
    if not candidate.is_relative_to(workspace.resolve()):
        raise HTTPException(status_code=400, detail="Path must stay inside the workspace")
    return candidate


def yaml_files(root: Path, folders: tuple[str, ...]) -> list[Path]:
    files: list[Path] = []
    for folder in folders:
        candidate = root / folder
        if candidate.exists():
            files.extend(sorted(candidate.rglob("*.yaml")))
    return files


def object_row(path: Path, root: Path) -> dict[str, Any]:
    data = read_yaml(path)
    row = dict(data)
    row.setdefault("id", path.stem)
    row.setdefault("name", path.stem)
    row.setdefault("type", "yaml")
    row["source"] = path.relative_to(root).as_posix() if path.is_relative_to(root) else path.as_posix()
    return row


def deep_merge(base: Any, patch: Any) -> Any:
    if isinstance(base, dict) and isinstance(patch, dict):
        merged = copy.deepcopy(base)
        for key, value in patch.items():
            if key in {"id", "type"}:
                continue
            merged[key] = deep_merge(merged.get(key), value)
        return merged
    return copy.deepcopy(patch)


def apply_object_patches(objects: dict[str, dict[str, Any]]) -> dict[str, dict[str, Any]]:
    patched = dict(objects)
    for obj in objects.values():
        if obj.get("type") != "object_patch":
            continue
        target_id = str(obj.get("target", ""))
        patch = obj.get("patch")
        if target_id and target_id in patched and isinstance(patch, dict):
            patched[target_id] = deep_merge(patched[target_id], patch)
    return patched


