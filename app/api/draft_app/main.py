@app.get("/api/framework/updates")
def framework_updates(workspace: Optional[str] = Query(default=None)) -> dict[str, Any]:
    root = resolve_workspace(workspace)
    lock_path = root / ".draft" / "framework.lock"
    lock = read_yaml(lock_path) if lock_path.exists() else {}
    current = framework_pin()
    pinned = ((lock.get("framework") or {}).get("pinnedCommit") if isinstance(lock.get("framework"), dict) else "")
    return {
        "workspace": str(root),
        "lockPath": str(lock_path),
        "locked": lock,
        "currentFramework": current,
        "updateAvailable": bool(pinned and current.get("pinnedCommit") and pinned != current.get("pinnedCommit")),
        "note": "This compares the workspace lock to the running framework checkout. Remote update checks should use the configured GitHub integration.",
    }


