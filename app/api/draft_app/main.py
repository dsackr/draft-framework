@app.post("/api/object-patches/upsert")
def upsert_object_patch(request: ObjectPatchWriteRequest) -> dict[str, Any]:
    root = resolve_workspace(request.workspace)
    patch_id = f"patch.{slug(request.target)}.{slug(request.purpose)}"
    document: dict[str, Any] = {
        "schemaVersion": "1.0",
        "id": patch_id,
        "type": "object_patch",
        "name": request.name or f"{request.target} {request.purpose}",
        "description": request.description or "App-generated object patch.",
        "catalogStatus": request.catalogStatus,
        "lifecycleStatus": request.lifecycleStatus,
        "target": request.target,
        "patch": request.patch,
    }
    if request.owner:
        document["owner"] = request.owner
    if request.tags:
        document["tags"] = request.tags
    output_path = object_output_path(root, document, "configurations", None)
    write_yaml(output_path, document)
    result: dict[str, Any] = {
        "ok": True,
        "workspace": str(root),
        "path": output_path.relative_to(root).as_posix(),
        "id": patch_id,
        "target": request.target,
    }
    if request.validateAfter:
        result["validation"] = validate_workspace_result(root)
    return result


