def find_disallowed_secret_keys(node: Any, path: str = "draftsman") -> list[str]:
    matches: list[str] = []
    if isinstance(node, dict):
        for key, value in node.items():
            next_path = f"{path}.{key}"
            if key in DISALLOWED_SECRET_KEYS:
                matches.append(next_path)
            matches.extend(find_disallowed_secret_keys(value, next_path))
    elif isinstance(node, list):
        for index, value in enumerate(node):
            matches.extend(find_disallowed_secret_keys(value, f"{path}[{index}]"))
    return matches


def draftsman_config_status(config: dict[str, Any]) -> dict[str, Any]:
    draftsman = config.get("draftsman")
    if not isinstance(draftsman, dict):
        draftsman = draftsman_default_config()
    draftsman = normalize_draftsman_config(draftsman)

    issues: list[str] = []
    mode = draftsman.get("mode", "external")
    if mode not in ALLOWED_DRAFTSMAN_MODES:
        issues.append(f"draftsman.mode must be one of {sorted(ALLOWED_DRAFTSMAN_MODES)}")

    embedded = draftsman.get("embedded", {})
    if not isinstance(embedded, dict):
        embedded = {}
    provider = embedded.get("provider", "openai")
    if provider not in ALLOWED_EMBEDDED_PROVIDERS:
        issues.append("Only OpenAI is allowed as an embedded Draftsman provider in this version")

    auth = embedded.get("auth", {})
    if not isinstance(auth, dict):
        auth = {}
    auth_type = auth.get("type", "oauth")
    if auth_type not in ALLOWED_EMBEDDED_AUTH_TYPES:
        issues.append("Only OAuth is allowed for embedded Draftsman auth; API keys are not allowed")
    if auth.get("apiKeysAllowed") is not False:
        issues.append("draftsman.embedded.auth.apiKeysAllowed must remain false")
    if auth.get("tokenStorage") in {"workspace", "file", "files"}:
        issues.append("OAuth tokens must not be stored in workspace files")

    secret_keys = find_disallowed_secret_keys(draftsman)
    if secret_keys:
        issues.append(f"Workspace config must not contain secret fields: {', '.join(secret_keys)}")

    auth_status = openai_codex_auth_status()
    embedded_available = bool(mode == "embedded" and auth_status["signedIn"])
    return {
        "ok": not issues,
        "mode": mode,
        "embeddedProvider": provider,
        "embeddedAuth": auth_type,
        "externalAgentAvailable": mode in {"external", "embedded"},
        "embeddedDraftsmanAvailable": embedded_available,
        "embeddedImplementationStatus": "available" if embedded_available else "sign-in-required",
        "oauthRuntimeConfigured": auth_status["signedIn"],
        "oauthClientId": auth.get("clientId", OPENAI_CODEX_CLIENT_ID),
        "oauthRedirectUri": auth.get("redirectUri", ""),
        "auth": auth_status,
        "issues": issues,
        "note": "Embedded Draftsman uses the signed-in user's ChatGPT/Codex OAuth session. Tokens are stored outside the workspace repo.",
    }


