def find_disallowed_secret_keys(node: Any, path: str = "draftsman") -> list[str]:
    matches: list[str] = []
    if isinstance(node, dict):
        for key, value in node.items():
            next_path = f"{path}.{key}"
            if key in DISALLOWED_SECRET_KEYS:
                matches.append(next_path)
            matches.extend(find_disallowed_secret_keys(value, next_path))
    elif isinstance(node, list):
        for index, value in enumerate(node):
            matches.extend(find_disallowed_secret_keys(value, f"{path}[{index}]"))
    return matches


def draftsman_config_status(config: dict[str, Any]) -> dict[str, Any]:
    draftsman = config.get("draftsman")
    if not isinstance(draftsman, dict):
        draftsman = draftsman_default_config()

    issues: list[str] = []
    mode = draftsman.get("mode", "external")
    if mode not in ALLOWED_DRAFTSMAN_MODES:
        issues.append(f"draftsman.mode must be one of {sorted(ALLOWED_DRAFTSMAN_MODES)}")

    embedded = draftsman.get("embedded", {})
    if not isinstance(embedded, dict):
        embedded = {}
    provider = embedded.get("provider", "openai")
    if provider not in ALLOWED_EMBEDDED_PROVIDERS:
        issues.append("Only OpenAI is allowed as an embedded Draftsman provider in this version")

    auth = embedded.get("auth", {})
    if not isinstance(auth, dict):
        auth = {}
    auth_type = auth.get("type", "oauth")
    if auth_type not in ALLOWED_EMBEDDED_AUTH_TYPES:
        issues.append("Only OAuth is allowed for embedded Draftsman auth; API keys are not allowed")
    if auth.get("apiKeysAllowed") is not False:
        issues.append("draftsman.embedded.auth.apiKeysAllowed must remain false")
    if auth.get("tokenStorage") in {"workspace", "file", "files"}:
        issues.append("OAuth tokens must not be stored in workspace files")

    secret_keys = find_disallowed_secret_keys(draftsman)
    if secret_keys:
        issues.append(f"Workspace config must not contain API key fields: {', '.join(secret_keys)}")

    oauth_env_ready = bool(
        os.environ.get(str(auth.get("clientIdRef", "")))
        and os.environ.get(str(auth.get("clientSecretRef", "")))
    )
    return {
        "ok": not issues,
        "mode": mode,
        "embeddedProvider": provider,
        "embeddedAuth": auth_type,
        "externalAgentAvailable": mode in {"external", "embedded"},
        "embeddedDraftsmanAvailable": False,
        "embeddedImplementationStatus": "configuration-only",
        "oauthRuntimeConfigured": oauth_env_ready,
        "issues": issues,
        "note": "External AI agents can use the repo and app API now. Embedded Draftsman model calls are not implemented until OAuth-based OpenAI access is confirmed for this use case.",
    }


def workspace_defaults(root: Path, request: WorkspaceInitRequest) -> tuple[dict[str, Any], dict[str, Any]]:
    framework = framework_pin()
    framework.update(request.framework or {})
    config = {
        "schemaVersion": "1.0",
        "workspace": {
            "name": request.name or root.name,
            "mode": "shared-app",
        },
        "repository": {
            "provider": "github",
            "owner": "",
            "name": root.name,
            "defaultBranch": "main",
            "devBranch": "dev",
            "publishMode": "pull-request",
        },
        "framework": framework,
        "paths": {
            "catalog": "catalog",
            "configurations": "configurations",
        },
        "auth": {
            "localMode": "gh",
            "sharedAppMode": "github-app",
        },
        "draftsman": draftsman_default_config(),
    }
    if request.repository:
        config["repository"].update(request.repository)
    lock = {
        "schemaVersion": "1.0",
        "framework": {
            **framework,
            "updatedAt": now_iso(),
        },
    }
    return config, lock


def initialize_workspace_files(root: Path, request: WorkspaceInitRequest) -> dict[str, Any]:
    root.mkdir(parents=True, exist_ok=True)
    for folder in CATALOG_FOLDERS:
        (root / "catalog" / folder).mkdir(parents=True, exist_ok=True)
    for folder in CONFIGURATION_FOLDERS:
        (root / "configurations" / folder).mkdir(parents=True, exist_ok=True)
    (root / ".draft").mkdir(parents=True, exist_ok=True)

    config, lock = workspace_defaults(root, request)
    config_path = root / ".draft" / "workspace.yaml"
    lock_path = root / ".draft" / "framework.lock"
    if not config_path.exists():
        write_yaml(config_path, config)
    if not lock_path.exists():
        write_yaml(lock_path, lock)
    return {
        "workspace": str(root),
        "created": True,
        "config": str(config_path),
        "lock": str(lock_path),
    }


def object_output_path(workspace: Path, document: dict[str, Any], layer: str, relative_path: Optional[str]) -> Path:
    if relative_path:
        return safe_workspace_path(workspace, relative_path)

    object_type = str(document.get("type", ""))
    object_id = str(document.get("id", ""))
    if not object_type or not object_id:
        raise HTTPException(status_code=400, detail="Document must declare id and type")
    default_layer, folder = OBJECT_TYPE_FOLDERS.get(object_type, ("catalog", "objects"))
    selected_layer = default_layer if layer == "auto" else layer
    if selected_layer not in {"catalog", "configurations"}:
        raise HTTPException(status_code=400, detail="Layer must be 'auto', 'catalog', or 'configurations'")
    if selected_layer != default_layer and object_type in {"compliance_framework", "compliance_profile", "domain", "object_patch", "odc"}:
        folder = OBJECT_TYPE_FOLDERS[object_type][1]
    return safe_workspace_path(workspace, f"{selected_layer}/{folder}/{slug(object_id)}.yaml")


def effective_model(workspace: Path) -> dict[str, Any]:
    base_config = FRAMEWORK_ROOT / "configurations"
    workspace_config = workspace / "configurations"
    workspace_catalog = workspace / "catalog"
    base_files = yaml_files(base_config, CONFIGURATION_FOLDERS)
    config_files = yaml_files(workspace_config, CONFIGURATION_FOLDERS)
    catalog_files = yaml_files(workspace_catalog, CATALOG_FOLDERS)
    objects: dict[str, dict[str, Any]] = {}
    for root, files, layer in (
        (base_config, base_files, "framework"),
        (workspace_config, config_files, "configuration"),
        (workspace_catalog, catalog_files, "catalog"),
    ):
        for path in files:
            row = object_row(path, root)
            row["layer"] = layer
            objects[str(row["id"])] = row
    objects = apply_object_patches(objects)
    return {
        "workspace": str(workspace),
        "layers": [
            {"name": "framework", "path": str(base_config), "count": len(base_files)},
            {"name": "configuration", "path": str(workspace_config), "count": len(config_files)},
            {"name": "catalog", "path": str(workspace_catalog), "count": len(catalog_files)},
        ],
        "objects": sorted(objects.values(), key=lambda item: str(item["id"])),
    }


