def initialize_workspace_files(root: Path, request: WorkspaceInitRequest) -> dict[str, Any]:
    root.mkdir(parents=True, exist_ok=True)
    for folder in CATALOG_FOLDERS:
        (root / "catalog" / folder).mkdir(parents=True, exist_ok=True)
    for folder in CONFIGURATION_FOLDERS:
        (root / "configurations" / folder).mkdir(parents=True, exist_ok=True)
    (root / ".draft").mkdir(parents=True, exist_ok=True)

    config, lock = workspace_defaults(root, request)
    config_path = root / ".draft" / "workspace.yaml"
    lock_path = root / ".draft" / "framework.lock"
    if not config_path.exists():
        write_yaml(config_path, config)
    if not lock_path.exists():
        write_yaml(lock_path, lock)
    return {
        "workspace": str(root),
        "created": True,
        "config": str(config_path),
        "lock": str(lock_path),
    }


def object_output_path(workspace: Path, document: dict[str, Any], layer: str, relative_path: Optional[str]) -> Path:
    if relative_path:
        return safe_workspace_path(workspace, relative_path)

    object_type = str(document.get("type", ""))
    object_id = str(document.get("id", ""))
    if not object_type or not object_id:
        raise HTTPException(status_code=400, detail="Document must declare id and type")
    default_layer, folder = OBJECT_TYPE_FOLDERS.get(object_type, ("catalog", "objects"))
    selected_layer = default_layer if layer == "auto" else layer
    if selected_layer not in {"catalog", "configurations"}:
        raise HTTPException(status_code=400, detail="Layer must be 'auto', 'catalog', or 'configurations'")
    if selected_layer != default_layer and object_type in {"compliance_framework", "compliance_profile", "domain", "object_patch", "odc"}:
        folder = OBJECT_TYPE_FOLDERS[object_type][1]
    return safe_workspace_path(workspace, f"{selected_layer}/{folder}/{slug(object_id)}.yaml")


def effective_model(workspace: Path) -> dict[str, Any]:
    base_config = FRAMEWORK_ROOT / "configurations"
    workspace_config = workspace / "configurations"
    workspace_catalog = workspace / "catalog"
    base_files = yaml_files(base_config, CONFIGURATION_FOLDERS)
    config_files = yaml_files(workspace_config, CONFIGURATION_FOLDERS)
    catalog_files = yaml_files(workspace_catalog, CATALOG_FOLDERS)
    objects: dict[str, dict[str, Any]] = {}
    for root, files, layer in (
        (base_config, base_files, "framework"),
        (workspace_config, config_files, "configuration"),
        (workspace_catalog, catalog_files, "catalog"),
    ):
        for path in files:
            row = object_row(path, root)
            row["layer"] = layer
            objects[str(row["id"])] = row
    objects = apply_object_patches(objects)
    return {
        "workspace": str(workspace),
        "layers": [
            {"name": "framework", "path": str(base_config), "count": len(base_files)},
            {"name": "configuration", "path": str(workspace_config), "count": len(config_files)},
            {"name": "catalog", "path": str(workspace_catalog), "count": len(catalog_files)},
        ],
        "objects": sorted(objects.values(), key=lambda item: str(item["id"])),
    }


def draftsman_context(workspace: Path, context_object_id: Optional[str]) -> str:
    model = effective_model(workspace)
    objects = model.get("objects", [])
    selected = None
    if context_object_id:
        selected = next((obj for obj in objects if obj.get("id") == context_object_id), None)
    object_rows = [
        {
            "id": obj.get("id"),
            "name": obj.get("name"),
            "type": obj.get("type"),
            "layer": obj.get("layer"),
            "source": obj.get("source"),
        }
        for obj in objects[:120]
    ]
    context = {
        "workspace": str(workspace),
        "framework": "DRAFT",
        "selectedObject": selected,
        "objectCount": len(objects),
        "objects": object_rows,
        "rules": [
            "Act as The Draftsman for DRAFT architecture content.",
            "Ask concise follow-up questions when ODC or compliance evidence is missing.",
            "Do not claim to write files directly; propose changes that the app/API can validate and write.",
            "Never ask for, reveal, or store API keys, OAuth tokens, Git credentials, or other secrets.",
            "Prefer reusable off-the-shelf catalog artifacts before creating new objects.",
        ],
    }
    return json.dumps(context, indent=2)


def call_embedded_draftsman(root: Path, config: dict[str, Any], request: DraftsmanChatRequest) -> dict[str, Any]:
    draftsman = normalize_draftsman_config(config.get("draftsman") if isinstance(config, dict) else {})
    model = str(((draftsman.get("embedded") or {}).get("model") or "gpt-5.5"))
    profile = active_openai_codex_profile()
    instructions = (
        "You are The Draftsman inside the DRAFT App. "
        "Help users document deployable architecture in the DRAFT framework. "
        "Use the provided effective-model context as source material. "
        "Be direct, ask needed questions, and produce concrete object or patch recommendations. "
        "Do not expose secrets and do not claim you have written files unless the app/API reports that action."
    )
    content: list[dict[str, Any]] = [
        {
            "type": "input_text",
            "text": f"Effective DRAFT context:\n{draftsman_context(root, request.contextObjectId)}\n\nUser request:\n{request.message}",
        }
    ]
    for attachment in request.attachments or []:
        if not isinstance(attachment, dict):
            continue
        name = str(attachment.get("name") or "attachment")
        media_type = str(attachment.get("type") or "")
        text = attachment.get("text")
        data_url = attachment.get("dataUrl")
        if media_type.startswith("image/") and isinstance(data_url, str) and data_url.startswith("data:image/"):
            content.append({"type": "input_image", "image_url": data_url})
            content.append({"type": "input_text", "text": f"Uploaded image: {name}"})
        elif isinstance(text, str):
            content.append({"type": "input_text", "text": f"Uploaded text file {name}:\n{text[:12000]}"})

    payload = {
        "model": model,
        "stream": True,
        "store": False,
        "instructions": instructions,
        "input": [
            {
                "role": "user",
                "content": content,
            }
        ],
    }
    response = stream_chatgpt_codex_response(payload, profile)
    return {
        "ok": True,
        "mode": "embedded",
        "provider": "openai-codex",
        "model": model,
        "account": auth_profile_public(profile),
        "message": response["text"],
    }


