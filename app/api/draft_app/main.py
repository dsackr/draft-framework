@app.post("/api/git/commit")
def git_commit(request: CommitRequest) -> dict[str, Any]:
    root = resolve_workspace(request.workspace)
    ensure_working_branch(root)
    paths = request.paths or ["catalog", "configurations", ".draft"]
    add = run(["git", "add", *paths], root)
    if add.returncode != 0:
        return {"ok": False, "stage": "add", "stdout": add.stdout, "stderr": add.stderr}
    commit = run(["git", "commit", "-m", request.message], root)
    return {"ok": commit.returncode == 0, "stdout": commit.stdout, "stderr": commit.stderr}


@app.post("/api/git/push")
def git_push(request: PushRequest) -> dict[str, Any]:
    root = resolve_workspace(request.workspace)
    branch = request.branch
    if not branch:
        current = run(["git", "branch", "--show-current"], root)
        branch = current.stdout.strip() if current.returncode == 0 else "dev"
    completed = run(["git", "push", request.remote, branch], root)
    return {"ok": completed.returncode == 0, "stdout": completed.stdout, "stderr": completed.stderr}


@app.post("/api/publish")
def publish(request: PublishRequest) -> dict[str, Any]:
    root = resolve_workspace(request.workspace)
    repo = workspace_repository_config(root)
    base = request.base or str(repo["defaultBranch"])
    head = request.head or str(repo["devBranch"])
    mode = request.mode or str(repo["publishMode"])
    if mode == "direct":
        completed = run(["git", "push", "origin", f"{head}:{base}"], root)
        return {"ok": completed.returncode == 0, "mode": "direct", "stdout": completed.stdout, "stderr": completed.stderr}
    completed = run(
        [
            "gh",
            "pr",
            "create",
            "--base",
            base,
            "--head",
            head,
            "--title",
            request.title,
            "--body",
            request.body,
        ],
        root,
    )
    return {"ok": completed.returncode == 0, "mode": "pull-request", "stdout": completed.stdout, "stderr": completed.stderr}


