def run(command: list[str], cwd: Path) -> subprocess.CompletedProcess[str]:
    try:
        return subprocess.run(
            command,
            cwd=str(cwd),
            text=True,
            capture_output=True,
            check=False,
        )
    except FileNotFoundError as exc:
        raise HTTPException(status_code=500, detail=f"Command not available: {command[0]}") from exc


def resolve_workspace(workspace: Optional[str] = None, must_exist: bool = True) -> Path:
    raw = workspace or os.environ.get("DRAFT_WORKSPACE") or str(DEFAULT_WORKSPACE)
    root = Path(raw).expanduser().resolve()
    if must_exist and not root.exists():
        raise HTTPException(status_code=404, detail=f"Workspace does not exist: {root}")
    return root


def workspace_config_path(workspace: Path) -> Path:
    return workspace / ".draft" / "workspace.yaml"


def read_yaml(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        data = yaml.safe_load(handle) or {}
    return data if isinstance(data, dict) else {}


def write_yaml(path: Path, data: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        yaml.safe_dump(data, handle, sort_keys=False, allow_unicode=False)


def now_iso() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def slug(value: str) -> str:
    return re.sub(r"[^a-z0-9.-]+", "-", value.lower()).strip(".-") or "object"


def safe_workspace_path(workspace: Path, relative_path: str) -> Path:
    candidate = (workspace / relative_path).resolve()
    if not candidate.is_relative_to(workspace.resolve()):
        raise HTTPException(status_code=400, detail="Path must stay inside the workspace")
    return candidate


def yaml_files(root: Path, folders: tuple[str, ...]) -> list[Path]:
    files: list[Path] = []
    for folder in folders:
        candidate = root / folder
        if candidate.exists():
            files.extend(sorted(candidate.rglob("*.yaml")))
    return files


def object_row(path: Path, root: Path) -> dict[str, Any]:
    data = read_yaml(path)
    row = dict(data)
    row.setdefault("id", path.stem)
    row.setdefault("name", path.stem)
    row.setdefault("type", "yaml")
    row["source"] = path.relative_to(root).as_posix() if path.is_relative_to(root) else path.as_posix()
    return row


def deep_merge(base: Any, patch: Any) -> Any:
    if isinstance(base, dict) and isinstance(patch, dict):
        merged = copy.deepcopy(base)
        for key, value in patch.items():
            if key in {"id", "type"}:
                continue
            merged[key] = deep_merge(merged.get(key), value)
        return merged
    return copy.deepcopy(patch)


def apply_object_patches(objects: dict[str, dict[str, Any]]) -> dict[str, dict[str, Any]]:
    patched = dict(objects)
    for obj in objects.values():
        if obj.get("type") != "object_patch":
            continue
        target_id = str(obj.get("target", ""))
        patch = obj.get("patch")
        if target_id and target_id in patched and isinstance(patch, dict):
            patched[target_id] = deep_merge(patched[target_id], patch)
    return patched


def validate_workspace_result(workspace: Path) -> dict[str, Any]:
    completed = run(["python3", str(FRAMEWORK_ROOT / "tools" / "validate.py"), "--workspace", str(workspace)], REPO_ROOT)
    return {
        "ok": completed.returncode == 0,
        "returnCode": completed.returncode,
        "stdout": completed.stdout,
        "stderr": completed.stderr,
    }


def framework_pin() -> dict[str, str]:
    commit = run(["git", "rev-parse", "HEAD"], REPO_ROOT)
    return {
        "source": "https://github.com/dsackr/draft-framework.git",
        "pinnedRef": "main",
        "pinnedCommit": commit.stdout.strip() if commit.returncode == 0 else "",
    }


