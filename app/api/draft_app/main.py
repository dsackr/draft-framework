@app.get("/api/catalog/browser")
def catalog_browser(workspace: Optional[str] = Query(default=None)) -> FileResponse:
    root = resolve_workspace(workspace)
    digest = hashlib.sha256(str(root).encode("utf-8")).hexdigest()[:16]
    output_path = Path(tempfile.gettempdir()) / f"draft-catalog-browser-{digest}.html"
    completed = run(
        [
            sys.executable,
            str(FRAMEWORK_ROOT / "tools" / "generate_browser.py"),
            "--workspace",
            str(root),
            "--output",
            str(output_path),
        ],
        REPO_ROOT,
    )
    if completed.returncode != 0:
        raise HTTPException(status_code=500, detail=completed.stderr or completed.stdout)
    return FileResponse(output_path, media_type="text/html")


